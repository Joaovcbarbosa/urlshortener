def enviar_requisicao(self, requisicao):
    requisicao = f"{requisicao}\r\n"
    self.send(requisicao.encode("utf-8"))

def enviar_cabelhaco(self, name, value):
    self.enviar_requisicao(f"{name}: {value}")
	
